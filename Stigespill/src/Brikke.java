
public class Brikke {

    private String id;
    private Rute rute;

    public Brikke(String id, Rute rute) {
        this.id = id;
        this.rute = rute;
    }


    public Rute getRute() {
        return rute;
    }

    public void setRute(Rute rute) {
        this.rute = rute;
    }

    public String getId() {
        return id;
    }


    public void setId(String id) {
        this.id = id;
    }

    


}
